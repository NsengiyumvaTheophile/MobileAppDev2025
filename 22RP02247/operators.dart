void main() {
  int a = 5;
  int b = 4;

  print("Addition: ${a + b}");
  print("Subtraction: ${a - b}");
  print("Multiplication: ${a * b}");
  print("Division: ${a / b}");
  print("Modulus: ${a % b}");

  
  bool isTrue = true;
  bool isFalse = false;
  print("AND: ${isTrue && isFalse}");
  print("OR: ${isTrue || isFalse}");
  print("NOT: ${!isTrue}");
}
