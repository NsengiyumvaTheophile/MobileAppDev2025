
// Function with Parameters
// This function is like a calculator that needs two numbers
// It adds them together
void addNumbers(int firstNumber, int secondNumber) {
  print("First number: $firstNumber");
  print("Second number: $secondNumber");
  print("Total: ${firstNumber + secondNumber}");
}


void main() {
  // Function with Parameters Example
  // This is like adding two numbers in a calculator
  addNumbers(3, 2);
}

