// Void Functions in Dart
// Definition: Functions that don't return any value. They are used for performing actions rather than computing values.

// Example 1: Basic void function
void greet() {
  print('Hello, World!');
}

// Example 2: Void function with parameters
void printUserInfo(String name, int age) {
  print('Name: $name');
  print('Age: $age');
}

