void main() {
  // final
  final String pacifiqueCity = 'Kigali';
  print('Pacifique lives in $pacifiqueCity');

  // dynamic
  dynamic pacifiqueVar = 'Hello!';
  print(pacifiqueVar);
  pacifiqueVar = 123;
  print(pacifiqueVar);

  // var
  var pacifiqueAge = 25;
  print('Pacifique is $pacifiqueAge years old');
}
