void main() {
  int a = 10;
  int b = 5;

  // Arithmetic Operators
  print('Addition: ${a + b}');
  print('Subtraction: ${a - b}');
  print('Multiplication: ${a * b}');
  print('Division: ${a / b}');
  print('Modulus: ${a % b}');

  // Relational Operators
  print('Equal: ${a == b}');
  print('Not Equal: ${a != b}');
  print('Greater: ${a > b}');
  print('Less: ${a < b}');

  // Logical Operators
  bool x = true;
  bool y = false;

  print('AND: ${x && y}');
  print('OR: ${(x || y)}'); 
  print('NOT: ${!x}');
}
