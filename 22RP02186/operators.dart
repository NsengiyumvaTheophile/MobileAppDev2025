void main() {
  int a = 20;
  int b = 5;

  // Arithmetic Operators
  print("Addition: ${a + b}");
  print("Subtraction: ${a - b}");
  print("Multiplication: ${a * b}");
  print("Division: ${a / b}");

  // Relational Operators
  print("a > b: ${a > b}");
  print("a == b: ${a == b}");

  // Logical Operators
  bool x = true;
  bool y = false;
  print("x && y: ${x && y}");
 
  print("!x: ${!x}");
}
