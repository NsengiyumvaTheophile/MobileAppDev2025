void main() {
  // This is a single-line comment
  int x = 5;

  /*
   This is a multi-line comment.
   It can span multiple lines.
  */
  int y = 10;

  /// This function adds two numbers
  int sum = x + y;

  print("Sum: $sum"); // Output the result
}
// This code demonstrates the use of comments in Dart.