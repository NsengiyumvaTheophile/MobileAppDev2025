// Functions with Parameters in Dart
// Definition: Functions that accept input values (parameters) to perform operations.



// Example 1: Function with single parameter
void greetPerson(String name) {
  print('Hello, $name!');
}

// Example 2: Function with multiple parameters
void calculateArea(double length, double width) {
  double area = length * width;
  print('Area of rectangle: $area');
}

// Example 3: Function with different parameter types
void printUserDetails(String name, int age, bool isStudent) {
  print('Name: $name');
  print('Age: $age');
  print('Is Student: $isStudent');
}

