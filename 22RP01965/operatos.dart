void main() {
  int a = 10;
  int b = 3;

  // Arithmetic Operators
  print('Add: ${a + b}');
  print('Subtract: ${a - b}');
  print('Multiply: ${a * b}');
  print('Divide: ${a / b}');
  print('Modulus: ${a % b}');

  // Comparison Operators
  print('Is a > b? ${a > b}');
  print('Is a == b? ${a == b}');

  // Logical Operators
  bool x = true;
  bool y = false;
  print('x && y = ${x && y}');
  print('x || y = ${x}');
}
