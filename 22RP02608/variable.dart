void main() {
  // Variable declaration
  var name = 'Alice';       // type inferred as String
  int age = 25;             // explicit type
  double height = 5.9;
  bool isStudent = true;
  dynamic anything = 'Hello'; // can change type later

  // Printing variables
  print('Name: $name');
  print('Age: $age');
  print('Height: $height');
  print('Is Student: $isStudent');
  print('Dynamic variable: $anything');
}
