// 5. Types of Functions


  // Definition: Dart supports different function types.

  // Syntax: N/A (conceptual)

  // Usage: Use based on need: regular, arrow, anonymous, higher-order, generator.

  // Example:

  // Regular: void greet() {}

  // Arrow: int square(int x) => x * x;

  // Anonymous: var sayHi = () => print("Hi");
  
// Regular functions: named functions defined normally.

// Anonymous functions: functions without a name, often used inline.

// Arrow functions: concise syntax for single-expression functions.

// Higher-order functions: functions that accept other functions as arguments or return functions.

// Generators: functions that generate a sequence of values (using sync* or async*).