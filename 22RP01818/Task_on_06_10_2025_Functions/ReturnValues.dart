  // 7. Return Values

  // Definition: A function can return a value to the caller.

  // Syntax:

  // returnType functionName() {
  //   return value;
  // }

  // Usage: To return results.

  // Example:
int multiply(int x, int y) {
  return x * y;
}

void main() {
  int result = multiply(4, 5);
  print(result); // 20
}
