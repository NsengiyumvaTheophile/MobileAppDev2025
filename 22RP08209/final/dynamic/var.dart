void main() {
  // Using final to declare a constant variable
final name="MANZI BAHIZI";
print(name);
// Using var to declare a variable that can change
var age = 25;
print(age);
// Changing the value of the variable
age = 30; 
print(age);
// Using dynamic to declare a variable that can hold any type
dynamic height = 5.9;
print(height);
// Changing the type of the dynamic variable
height = "Tall";
print(height);
}